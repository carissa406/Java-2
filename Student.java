package Labs;

public class Student {
	private String strFirstName, strLastName, strMajor;
	
	public Student(String strFirstName, String strLastName, String strMajor) {
		this.strFirstName = strFirstName;
		this.strLastName = strLastName;
		this.strMajor = strMajor;
	}

	public String getStrFirstName() {
		return strFirstName;
	}

	public void setStrFirstName(String strFirstName) {
		this.strFirstName = strFirstName;
	}

	public String getStrLastName() {
		return strLastName;
	}

	public void setStrLastName(String strLastName) {
		this.strLastName = strLastName;
	}

	public String getStrMajor() {
		return strMajor;
	}

	public void setStrMajor(String strMajor) {
		this.strMajor = strMajor;
	}
	
	
	

}
